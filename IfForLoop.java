package week1.day1;

public class IfForLoop {

	public static void main(String[] args) {
		
		// 1 to 20 ---> 0dd number
		
     for (int i = 1; i < 20; i++) {
    	 
    	 
    	 // i%2==1, i%2!=0,  
    	 
    	 if (i%2==1) {
    		 
    		 System.out.println("Odd Number: " +i);
			
		}
    	 
    	 else {
			
    		 System.out.println("even number: " +i);
		}
		
	}
}
}